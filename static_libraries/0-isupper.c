#include <stdio.h>
/**
 * _isupper - This function checks for uppercase character
 *
 * @c: number to check
 *
 * Return: 1 if c is uppercase
 *         0 otherwise
 */

int _isupper(int c)
{
	int var;

	if (c > 64 && c < 91)
	{
		var = 1;
	} else
	{
		var = 0;
	}
	return (var);
}
