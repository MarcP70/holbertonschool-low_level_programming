#include <stdio.h>
#include "3-calc.h"

/**
 * op_add - This function added two integers
 * @a: the first integer
 * @b: the second integer
 * Return: the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - This function subtracted two integers
 * @a: the first integer
 * @b: the second integer
 * Return: the difference
 */
int op_sub(int a, int b)
{
        return (a - b);
}

/**
 * op_mul - This function multiplied two integers
 * @a: the first integer
 * @b: the second integer
 * Return: the product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - This function divided two integers
 * @a: the first integer
 * @b: the second integer
 * Return: the result of the division
 */
int op_div(int a, int b)
{
        return (a / b);
}

/**
 * op_mod - This function does the modulo of two integers
 * @a: the first integer
 * @b: the second integer
 * Return: the remainder of the division
 */
int op_sub(int a, int b)
{
        return (a % b);
}
